    private void cargarPartidos() {
        OperacionesPartido oper = new OperacionesPartido();
        this.listadoPartidos = oper.consultarAll();
        DefaultTableModel modelo = new DefaultTableModel();
        String[] columnas = {"ID", "FECHA", "LOCAL ", "GOL LOCAL","VISITANTE", "GOL VISITANTE"};

        modelo.setColumnIdentifiers(columnas);
        
        for (int i = 0; i < listadoPartidos.size(); i++) {
            Partido dato = listadoPartidos.get(i);
            Object [] fila = new Object[6];
            fila[0] = dato.getId();
            fila[1] = Utilidades.formatoFecha( dato.getFecha()) ;
            fila[2] = dato.getEquipoLocal().getNombre();
            fila[3] = dato.getGolesLocal();
            fila[4] = dato.getEquipoVisitante().getNombre();
            fila[5] = dato.getGolesVisitante();
             modelo.addRow(fila);
        }
        
        tbEquipos.setModel(modelo);    
    }
