    private void crear() {
        
        if (this.cjElocal.getSelectedIndex()<0 || this.cjElocal.getSelectedIndex()<0 || 
                this.fechaSel.getDate() == null || this.cjGolL.getText().isEmpty() || this.cjGolV.getText().isEmpty()){
            this.mensajeWarning("Algun valor requerido esta faltando. ¡Verifique!");
            return ;
        }
        if (((String)this.cjElocal.getSelectedItem()).equals(((String)this.cjElVisita.getSelectedItem()))){
            this.mensajeWarning("El equipo Local y visitante son iguales. ¡Verifique!");
            return ;
        }
        OperacionesPartido oper = new OperacionesPartido();
        Partido e = new Partido();
        e.setFecha(this.fechaSel.getDate());
        e.setEquipoLocal(this.listadoEquiposM.get((String)this.cjElocal.getSelectedItem()));
        e.setGolesLocal(Utilidades.convertirNumeroI(this.cjGolL.getText()));
        e.setEquipoVisitante(this.listadoEquiposM.get((String)this.cjElVisita.getSelectedItem()));
        e.setGolesVisitante(Utilidades.convertirNumeroI(this.cjGolV.getText()));

        int x = oper.crear(e);
        if (x >0){
            mensajeOK("Se creó el partido ");
            cargarPartidos();
            return ;
        }else{
            mensajeWarning("No se creó el partido, verifique e intente de nuevo. ");
        }
            
    }
    private void mensajeOK(String mensaje){
        JOptionPane.showMessageDialog(this, mensaje, "Información", JOptionPane.INFORMATION_MESSAGE);
    }
    private void mensajeWarning(String mensaje){
        JOptionPane.showMessageDialog(this, mensaje, "Información", JOptionPane.WARNING_MESSAGE);
    }

    private void cargarEquipos() {
        OperacionesEquipo oper = new OperacionesEquipo();
        
        List<Equipo> listaE =  oper.consultarAll();
        for (int i = 0; i < listaE.size(); i++) {
            Equipo dato = listaE.get(i);
            listadoEquiposM.put(dato.getNombre(), dato);
            this.cjElocal.addItem(dato.getNombre());
            this.cjElVisita.addItem(dato.getNombre());
        }
        
        
        
        
    }

    private void editar() {
        int index = tbEquipos.getSelectedRow();
        OperacionesPartido oper = new OperacionesPartido();
        Partido e = new Partido();
        e.setId(Utilidades.convertirNumeroL(this.cjId.getText()));
        e.setGolesLocal(Utilidades.convertirNumeroI(this.cjGolL.getText()));
        e.setGolesVisitante(Utilidades.convertirNumeroI(this.cjGolV.getText()));
        
        int x = oper.editar(e);
        if (x >0){
            mensajeOK("Se cambio la información de goles del partido ");
            cargarPartidos();
            this.limpiar();
            return ;
        }else{
            mensajeWarning("No se actualizó la información del partido, verifique e intente de nuevo. ");
        }
    }
    private void borrar() {
        int indice = tbEquipos.getSelectedRow();
        if (indice < 0){
            this.mensajeWarning("Seleccione el partido que desea borrar. ");
            return ;
        }
        String[] optiones = {"Si", "No"};
        int rta = JOptionPane.showOptionDialog(null, "¿Desea borrar el equipo ?", "Información", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, optiones, optiones[1]);
         if (rta == 0) {
             
             String id = tbEquipos.getValueAt(indice, 0).toString();
             OperacionesPartido oper = new OperacionesPartido();
             Partido e = new Partido();
             e.setId(new Long(id));
             int rta1 = oper.borrar(e);
             if (rta1 > 0 ){
                 this.mensajeOK("Se borró el partido exitosamente.");
                 cargarPartidos();
             }else{
                 this.mensajeWarning("No se borró el partido. ¡Verifique!");
             }
             
         } 
        
    }

    private void cambiovalor() {
        this.lTitulo.setText("Modifique la información del partido. ");
        this.cjId.setText(tbEquipos.getValueAt(tbEquipos.getSelectedRow(), 0).toString());
        String fecha = tbEquipos.getValueAt(tbEquipos.getSelectedRow(), 1).toString();
        Date laFecha = Utilidades.obtenerFecha("dd/MM/yyyy", fecha);
        if (laFecha != null){
            fechaSel.setDate(laFecha);
        }
        String eqLocal = tbEquipos.getValueAt(tbEquipos.getSelectedRow(), 2).toString();
        this.cjElocal.setSelectedItem(eqLocal);
        this.cjGolL.setText(tbEquipos.getValueAt(tbEquipos.getSelectedRow(), 3).toString());
        String eqVisit = tbEquipos.getValueAt(tbEquipos.getSelectedRow(), 4).toString();
        this.cjElVisita.setSelectedItem(eqVisit);
        this.cjGolV.setText(tbEquipos.getValueAt(tbEquipos.getSelectedRow(), 5).toString());
        
        this.pCrear.setVisible(true);
        this.btEditar.setVisible(true);
        this.btCrear.setVisible(false);
        this.pCrear.setBackground(new Color(190, 190, 190));
        
    }

